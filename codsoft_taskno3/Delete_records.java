        try
        {
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","muni");
            Statement st=con.createStatement();
            Statement stt=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student where rollno='"+rollno+"'");
            ResultSet rss=stt.executeQuery("select * from result where rollno='"+rollno+"'");
           
            if(rs.next() || rss.next())
              {
                
                st.executeUpdate("delete from result where rollno='"+rollno+"'");
                 st.executeUpdate("delete from student where rollno='"+rollno+"'");
                JOptionPane.showMessageDialog(null, "Successfully Updated");
                setVisible(false);
                new insertnewresult().setVisible(true);
            
              }
            else
              {
                
                JOptionPane.showMessageDialog(null, "Roll number does not exist in our database");
            
              }
        }
